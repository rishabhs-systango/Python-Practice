# calculating grade through percentage and pass or fail
percentage = float(input('Enter your percentage\n'))
if percentage >= 90 :
    print('you are pass and your grade is A+ \n')
elif percentage >=80 :
     print('you are pass and your grade is A \n')
elif percentage >=70 :
     print('you are pass and your grade is B+\n')
elif percentage >=60 :
     print('you are pass and your grade is B \n')
elif percentage >=50 :
     print('you are pass and your grade is C\n')
elif percentage >=40 :
     print('you are pass and your grade is D \n')
elif percentage <40 :
     print('you are Fail and your grade is F \n')